export const resolveModelProfile = (model, profileId) => {
  const profile = model.profiles?.find((candidate) => candidate.id === profileId) ?? model.profiles?.[0]
  return profile ? { ...model, ...profile, id: model.id, profileId: profile.id } : model
}

