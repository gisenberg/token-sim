export const flattenToolSteps = (steps) => steps.map((step) => {
  if (!Array.isArray(step)) return step
  return {
    label: step.map((entry) => entry.label).join(' + '),
    parallel: step.map((entry) => entry.label),
    thinkTokens: step.reduce((sum, entry) => sum + entry.thinkTokens, 0),
    decodeTokens: step.reduce((sum, entry) => sum + entry.decodeTokens, 0),
    execMs: Math.max(...step.map((entry) => entry.execMs)),
    resultTokens: step.reduce((sum, entry) => sum + entry.resultTokens, 0),
  }
})

